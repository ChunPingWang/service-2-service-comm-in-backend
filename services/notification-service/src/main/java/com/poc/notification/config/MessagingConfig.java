package com.poc.notification.config;

import org.apache.kafka.clients.consumer.ConsumerConfig;
import org.apache.kafka.clients.producer.ProducerConfig;
import org.apache.kafka.common.TopicPartition;
import org.apache.kafka.common.serialization.StringDeserializer;
import org.apache.kafka.common.serialization.StringSerializer;
import org.springframework.amqp.core.Binding;
import org.springframework.amqp.core.BindingBuilder;
import org.springframework.amqp.core.DirectExchange;
import org.springframework.amqp.core.Queue;
import org.springframework.amqp.core.QueueBuilder;
import org.springframework.amqp.core.TopicExchange;
import org.springframework.amqp.support.converter.JacksonJsonMessageConverter;
import org.springframework.amqp.support.converter.MessageConverter;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.kafka.annotation.EnableKafka;
import org.springframework.kafka.config.ConcurrentKafkaListenerContainerFactory;
import org.springframework.kafka.core.ConsumerFactory;
import org.springframework.kafka.core.DefaultKafkaConsumerFactory;
import org.springframework.kafka.core.DefaultKafkaProducerFactory;
import org.springframework.kafka.core.KafkaTemplate;
import org.springframework.kafka.core.ProducerFactory;
import org.springframework.kafka.listener.CommonErrorHandler;
import org.springframework.kafka.listener.ContainerProperties;
import org.springframework.kafka.listener.DeadLetterPublishingRecoverer;
import org.springframework.kafka.listener.DefaultErrorHandler;
import org.springframework.util.backoff.FixedBackOff;

import java.util.HashMap;
import java.util.Map;

/**
 * Messaging configuration for Kafka consumer and RabbitMQ producer.
 *
 * <p>Configures:
 * <ul>
 *   <li>Kafka consumer factory with StringDeserializer and group-id "notification-service"</li>
 *   <li>Kafka listener container factory with MANUAL acknowledgment mode</li>
 *   <li>Kafka DLQ support via {@link DefaultErrorHandler} with {@link DeadLetterPublishingRecoverer}</li>
 *   <li>Kafka producer factory and template (for test support and future publishing)</li>
 *   <li>RabbitMQ exchange, queue, and binding for shipping notifications</li>
 *   <li>Jackson JSON message converter for RabbitMQ</li>
 * </ul>
 */
@EnableKafka
@Configuration
public class MessagingConfig {

    @Value("${spring.kafka.bootstrap-servers:localhost:9092}")
    private String bootstrapServers;

    @Value("${spring.kafka.consumer.group-id:notification-service}")
    private String groupId;

    @Value("${spring.kafka.consumer.auto-offset-reset:earliest}")
    private String autoOffsetReset;

    // ── Kafka ─────────────────────────────────────────────────────────────────────

    @Bean
    public ConsumerFactory<String, String> consumerFactory() {
        Map<String, Object> props = new HashMap<>();
        props.put(ConsumerConfig.BOOTSTRAP_SERVERS_CONFIG, bootstrapServers);
        props.put(ConsumerConfig.GROUP_ID_CONFIG, groupId);
        props.put(ConsumerConfig.AUTO_OFFSET_RESET_CONFIG, autoOffsetReset);
        props.put(ConsumerConfig.KEY_DESERIALIZER_CLASS_CONFIG, StringDeserializer.class);
        props.put(ConsumerConfig.VALUE_DESERIALIZER_CLASS_CONFIG, StringDeserializer.class);
        return new DefaultKafkaConsumerFactory<>(props);
    }

    @Bean
    public ConcurrentKafkaListenerContainerFactory<String, String> kafkaListenerContainerFactory() {
        ConcurrentKafkaListenerContainerFactory<String, String> factory =
                new ConcurrentKafkaListenerContainerFactory<>();
        factory.setConsumerFactory(consumerFactory());
        factory.getContainerProperties().setAckMode(ContainerProperties.AckMode.MANUAL);
        factory.setCommonErrorHandler(kafkaErrorHandler());
        return factory;
    }

    /**
     * Configures a {@link DefaultErrorHandler} with DLQ support.
     *
     * <p>Failed messages are retried up to 3 times with a 1-second fixed backoff.
     * After exhausting retries, the message is published to {@code <original-topic>.dlq}.</p>
     */
    @Bean
    public CommonErrorHandler kafkaErrorHandler() {
        DeadLetterPublishingRecoverer recoverer = new DeadLetterPublishingRecoverer(
                kafkaTemplate(),
                (record, exception) -> new TopicPartition(record.topic() + ".dlq", record.partition()));
        return new DefaultErrorHandler(recoverer, new FixedBackOff(1000L, 3));
    }

    @Bean
    public ProducerFactory<String, String> producerFactory() {
        Map<String, Object> props = new HashMap<>();
        props.put(ProducerConfig.BOOTSTRAP_SERVERS_CONFIG, bootstrapServers);
        props.put(ProducerConfig.KEY_SERIALIZER_CLASS_CONFIG, StringSerializer.class);
        props.put(ProducerConfig.VALUE_SERIALIZER_CLASS_CONFIG, StringSerializer.class);
        return new DefaultKafkaProducerFactory<>(props);
    }

    @Bean
    public KafkaTemplate<String, String> kafkaTemplate() {
        return new KafkaTemplate<>(producerFactory());
    }

    // ── RabbitMQ ──────────────────────────────────────────────────────────────────

    @Bean
    public TopicExchange shippingExchange() {
        return new TopicExchange("shipping.exchange");
    }

    /**
     * Main shipping queue configured with dead-letter routing.
     *
     * <p>Must match the queue declaration in the Shipping Service to avoid
     * RabbitMQ property mismatch errors.</p>
     */
    @Bean
    public Queue shippingQueue() {
        return QueueBuilder.durable("shipping.queue")
                .withArgument("x-dead-letter-exchange", "shipping.exchange.dlq")
                .withArgument("x-dead-letter-routing-key", "shipping.dlq")
                .build();
    }

    @Bean
    public Binding shippingBinding(Queue shippingQueue, TopicExchange shippingExchange) {
        return BindingBuilder.bind(shippingQueue).to(shippingExchange).with("shipping.notification");
    }

    // ── Dead Letter Queue ─────────────────────────────────────────────────────────

    @Bean
    public DirectExchange shippingDlqExchange() {
        return new DirectExchange("shipping.exchange.dlq");
    }

    @Bean
    public Queue shippingDlqQueue() {
        return QueueBuilder.durable("shipping.queue.dlq").build();
    }

    @Bean
    public Binding shippingDlqBinding(Queue shippingDlqQueue, DirectExchange shippingDlqExchange) {
        return BindingBuilder.bind(shippingDlqQueue).to(shippingDlqExchange).with("shipping.dlq");
    }

    // ── Message Converter ─────────────────────────────────────────────────────────

    @Bean
    public MessageConverter jacksonJsonMessageConverter() {
        return new JacksonJsonMessageConverter();
    }
}
